                                <p><span style="color: rgba(0, 128, 0, 0.884); font-size: 3rem;font-weight:bold;">Prevention</span><br>
                                    
                                    Control and prevention of lumpy skin disease relies on
                                    four tactics - movement control (quarantine), vaccination,
                                    slaughter campaigns and management strategies. Specific
                                    national control plans vary between countries and so
                                    advice should be sought from the relevant authorities and
                                    veterinarians.
                                    Vaccination is the most effective means of control, and
                                    live homologous vaccines containing a Neethling-like
                                    strain of LSDV are recommended.
                                    Treatment
                                    There is no treatment for the virus, so prevention by
                                    vaccination is the most effective means of control.
                                    Secondary infections in the skin may be treated with
                                    Non-Steroidal Anti-Inflammatories (NSAIDs) and also
                                    antibiotics (topical +/- injectable) when appropriate.
                                    Adapted from the original article from eMergence' (
                                    http://www.emergence-msd-animal-health.com/
                                    Summary
                                    Lumpy skin disease virus causes a severe disease in
                                    cattle characterised by nodules in the skin. Transmission
                                    of LSD occurs via insect vectors and vaccination is the
                                    most effective means of control. During the past five
                                    years lumpy skin disease has spread through the Middle
                                    East into southeast Europe, the Caucasus, southwest
                                    Russia and western Asia. The disease causes substantial
                                    losses in affected herds with significant economic
                                    consequences. It also blocks access of affected countries
                                    to lucrative export markets, compounding the financial
                                    impact of a LSD outbreak. The main lesson to be learnt
                                    from the current European LSD epidemic is to be vigilant
