                    <p><span style="color: rgba(0, 128, 0, 0.884); font-size: 3rem;font-weight:bold;">Cause</span><br>
                        Lumpy skin disease (LSD) is caused by infection of cattle or
                        water buffalo with the poxvirus Lumpy skin disease virus
                        (LSDV). The virus is one of three closely related species
                        within the genus capripoxvirus, the other two species being
                        Sheeppox virus and Goatpox virus .
                        LSD was first described in Zambia in 1929. Over the next 85
                        years it steadily spread throughout the majority of Africa and
                        into the Middle East. In 2015 the virus entered mainland
                        Europe in Greece, and the Caucasus and Russia. In 2016 the
                        virus spread further east into the Balkans, north towards
                        Moscow, and west into Kazakhstan. It is currently
                        considered a rapidly emerging disease of high consequence.
                        It is notifiable with outbreaks causing significant damage to
                        productivity and trade.
                        Sponsor Content<br>
                        <span style="color: rgba(0, 128, 0, 0.884); font-size: 3rem; font-weight: bold;">Transmission</span><br>
                        There is still a good deal of information lacking about the
                        transmission of LSD. Experimental work has shown that
                        direct transmission from an infected to a naïve animal is very
                        inefficient. Evidence to date supports transmission of the
                        virus via arthropods such as insects or ticks (these are termed
                        virus "vectors"). For example outbreaks of LSD occur during
                        warm, wet weather while the disease usually diminishes in
                        the cooler winter months. In addition, LSD epidemics are
                        often characterised by new outbreaks occurring at distances
                        over 50km from the nearest known disease focus. These
                        characteristics strongly suggest insect-borne transmission,
                        such as mosquitoes and ticks. However, it is unclear which
                        vector species are involved in transmission of LSD, and if it
                        is a simple mechanical transmission of the virus or a more
                        complicated biological transmission involving replication or
                        development of the virus in the vector.
                        Movement of infected cattle can also be a significant factor
                        in the spread of LSD over large distances.</p>
